public class Practice1{
    public static void main(String[] args) {
        // if conditions
        int a = 10;
        if (a == 10){
            System.out.println("Hello.");
        }

        // while loop
        int count = 1;
        while (count != 5){
            System.out.println(count);
            count++;
        }

        // for loop
        for(int count1=1; count1!=5; count1++){
            System.out.println(count1);
        }
    }
}
